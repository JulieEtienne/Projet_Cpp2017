#include "BacterieS.h"


// ===========================================================================
//                             Constructors
// ===========================================================================
BacterieS::BacterieS() : Bacteria(1)
{
    rBB = 0.1;
    rBC = 0.1;
}

// ===========================================================================
//                             Destructor
// ===========================================================================
BacterieS::~BacterieS() {}

// ===========================================================================
//                             Public Methods
// ===========================================================================
void BacterieS::metabolism(float &out)
{
    out -= out * 0.1 * rBB;
    B += out * 0.1 * rBB - B * 0.1 * rBC;
    C += B * 0.1 * rBC;
}
