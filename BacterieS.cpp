// ===========================================================================
//                             Includes
// ===========================================================================
#include "BacterieS.h"


// ===========================================================================
//                             Constructors
// ===========================================================================
BacterieS::BacterieS() : Bacteria(1)
{
    rBB = 0.1;
    rBC = 0.1;
}


// ===========================================================================
//                             Destructor
// ===========================================================================
BacterieS::~BacterieS() {}

// ===========================================================================
//                             Public Methods
// ===========================================================================
void BacterieS::metabolism(float B_out)
{
    B_out *= (1 - rBB);
    B = B_out * rBB + B * (1 - rBC);
    C += B * rBC;
}
