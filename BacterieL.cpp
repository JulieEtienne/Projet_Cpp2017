#include "BacterieL.h"


// ===========================================================================
//                             Constructors
// ===========================================================================
BacterieL::BacterieL() : Bacteria(0)
{
    rAA = 0.1;
    rAB = 0.1;
}

// ===========================================================================
//                             Destructor
// ===========================================================================
BacterieL::~BacterieL() {}

// ===========================================================================
//                             Public Methods
// ===========================================================================
void BacterieL::metabolism(float A_out)
{
    A_out *= (1 - rAA);
    A = A_out * rAA + A * (1 - rAB);
    B += A * rAB;
}
